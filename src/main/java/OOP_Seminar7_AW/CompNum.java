package OOP_Seminar7_AW;

public class CompNum {
    private double real;
    private double imag;

    public CompNum(double real, double imag){
        this.real = real;
        this.imag = imag;
    }

    public double getReal() {
        return real;
    }

    public double getImag() {
        return imag;
    }
}
